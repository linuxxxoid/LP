    check_relation(Res),!,
    check_link(X, Y, Res).


question_word(X) :- member(X, [who, whose, chei, chya, kto]).

verb(X) :- member(X, [is, are]).

apostrophe(X) :- member(X, ["'s"]).

question_mark(X) :- member(X, ['?']).


